package exercicio1;

public class App {

	public static void main(String[] args) {
		
		int indice = 13;
		int soma = 0;
		int k = 0;
		
		while(k < indice) {
			k += 1;
			soma += k;
		}

		System.out.println(soma);
		
		// Ao final do processamento , qual sera o valor da variavel SOMA?
		// Resposta: 91
		
	}

}
